                            <h4 class="text-primary">Sejarah</h4>
                            <h1 class="display-4 mb-4">Sejarah Perpustakaan Umum Kota Kediri</h1>
                            <p>Perpustakaan umum pada awalnya merupakan taman bacaan yang dikelola oleh perkumpulan pemuda Cina yang bertempat di Jalan Brawijaya, Kediri. Karena pengurus perkumpulan tersebut terlalu sibuk dengan berbagai kegiatan, taman bacaan menjadi kurang berkembang. Oleh karena itu, pada tahun 1963 taman bacaan tersebut diserahkan kepada Walikota Kediri yang saat itu dijabat oleh Bapak Anwar Zainuddin untuk dikelola dan dimanfaatkan demi bertambahnya pengetahuan masyarakat umum di wilayah Kota Kediri.
                            </p>
                            <p>Seiring dengan penyerahan pengelolaan taman bacaan tersebut, taman bacaan tersebut berubah nama menjadi Perpustakaan Umum dan bergabung dalam jajaran pemerintahan kotamadya Kediri di bawah naungan Sub Bagian Sosial Bagian Kesejahteraan Rakyat, sedangkan lokasi Perpustakaan Umum tetap berada di jalan Brawijaya, Kediri. Pada tahun 1971, lokasi Perpustakaan Umum dipindahkan ke jalan Diponegoro, no.1, Kediri, dan dijadikan satu dengan sanggar pramuka. Pada tahun yang sama, organisasi Perpustakaan Umum tetap menjadi bagian dari Bagian Sosial Pemerintah Kota Kediri (perubahan nomenklatur dari Bagian Kesejahteraan Rakyat).
                            </p>
                            <p>Pada tahun 1995, gedung Perpustakaan Umum di jalan Diponegoro, No.1, Kediri,  dibangun/dipugar atas bantuan Mastrip, sehingga untuk sementara kegiatan perpustakaan umum berhenti, sedangkan buku-buku koleksi pepustakaan ditempatkan/disimpan di Bagian Sosial Pemerintah Kota Kediri. Pada tahun 1996, setelah pembangunan gedung selesai, Perpustakaan Umum kembali menempati gedung di jalan Diponegoro No. 1 Kediri (Gedung Graha Mastrip). Seiring dengan penempatan kembali gedung perpustakaan, tepatnya pada tanggal 8 desember 1996 diresmikan menjadi Perpustakaan Umum Kota Kediri yang secara organisasi telah lepas dari Bagian Sosial. Mulai tahun 1996, Perpustakaan Umum Kota Kediri berstatus sebagai UPD (Unit Pelaksana Daerah).
                            </p>
                            <p>Seiring dengan pelaksanaan otonomi daerah, pada tahun 2001 Perpustakaan Umum Kota Kediri bergabung menjadi satu dalam Dinas Informasi, Komunikasi, dan Pariwisata (Inkoparta) Kota Kediri sebagai seksi perpustakaan dokumen daerah dan PDE Sub Dinas Hubungan Masyarakat Dinas Inkopatra Kota Kediri sebagaimana tercantum dalam peraturan Daerah (Perda) Nomor 11 Tahun 2001 tentang Struktur Organisasi Dinas.
                            </p>
                            <p>Pada tahun 2004 dalam lingkup pemerintah kota kediri diadakan perubahan organisasai perangkat daerah dimana Dinas Informasi, komunikasi dan pariwisata di hapus dan perpustakaan umum bergabung dalam dinas pendidikan kota kediri sebagai Unit Pelaksana Teknis Dinas (UPTD). Setelah pada tahun 2008 dengan diberlakukannya Peraturan Pemerintah Nomor 8 tahun 2003 tentang pedoman organisai perangkat daerah maka berdasar peraturan daerah Nomor 6 tahun 2007  dibentuk kantor Arsip dan perpustakaan Kota Kediri. Dan pada tahun 2009 dengan berdasar pada peraturan Daerah Nomor 6 tahun 2008 nomenklatur Kantor Arsip dan perpustakaan berubah menjadi kantor perpustakaan, Arsip dan Dokumentasi Kota Kediri.
                            </p>
